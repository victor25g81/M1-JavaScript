// ¿Cómo crear variables en JS?

// 1. Utilizar var:

var nombre = 'Víctor';
var apellido = 'De la fuente';
var email = 'victor@mail.co';

console.log('Nombre: ', nombre);
console.log('Apellido: ', apellido);
console.log('Email: ', email);

// 2. Utilizar let:

let profesion = 'Programador';
let cantidadHijos = 1;
let paisResidencia = 'México';

console.log('Profesión:', profesion);
console.log('Apellido:', apellido);
console.log('País residencia:', paisResidencia);

// 3. Crear constantes:

const paisNacimiento = 'México';
const cantidadHermanos = 4;

console.log('País nacimiento:', paisNacimiento);
console.log('Cantidad hermanos:', cantidadHermanos);

